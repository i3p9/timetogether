export function getCountryFlag(country: string): string {
	// Try exact match first
	const flag = countryToFlag[country];
	if (flag) return flag;

	// Try case-insensitive match
	const lowerCountry = country.toLowerCase();
	for (const [key, value] of Object.entries(countryToFlag)) {
		if (key.toLowerCase() === lowerCountry) {
			return value;
		}
	}

	// Default to globe icon if no flag found
	return "ðŸŒ";
}

import { TIMEZONE_DATA } from "../timezones";

export const getCountryForTimezone = (timezoneId: string): string => {
	const timezoneData = TIMEZONE_DATA.find(
		(tz) => tz.id === timezoneId
	);
	return timezoneData?.country || "Unknown";
};
